const person = {
  name: 'John',
  age: 30,
  city: 'New York'
};

// Accessing values using dot notation
console.log(person.name); // Output: John
console.log(person.age); // Output: 30
console.log(person.city); // Output: New York

// Accessing values using square bracket notation
console.log(person['name']); // Output: John
console.log(person['age']); // Output: 30
console.log(person['city']); // Output: New York
