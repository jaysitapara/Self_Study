// Arithmetic Operators
const addition = 5 + 3; // 8
const subtraction = 10 - 4; // 6
const multiplication = 2 * 6; // 12
const division = 15 / 3; // 5
const modulo = 17 % 4; // 1

// Comparison Operators
const isEqual = 5 == "5"; // true (type coercion)
const isStrictEqual = 5 === "5"; // false (no type coercion)
const isNotEqual = 10 != 5; // true (type coercion)
const isStrictNotEqual = 10 !== "10"; // true (no type coercion)
const isGreaterThan = 8 > 3; // true
const isLessThan = 4 < 9; // true
const isGreaterThanOrEqual = 7 >= 7; // true
const isLessThanOrEqual = 6 <= 5; // false

// Logical Operators
const logicalAnd = true && false; // false
const logicalOr = true || false; // true
const logicalNot = !true; // false

// Assignment Operators
let x = 5; // x = 5
x += 3; // x = x + 3 => x = 8
x -= 2; // x = x - 2 => x = 6
x *= 4; // x = x * 4 => x = 24
x /= 6; // x = x / 6 => x = 4
x %= 3; // x = x % 3 => x = 1

// Unary Operators
const unaryPlus = +5; // 5
const unaryMinus = -10; // -10
let y = 7;
y++; // y = y + 1 => y = 8
y--; // y = y - 1 => y = 7

